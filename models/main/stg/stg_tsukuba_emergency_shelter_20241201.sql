SELECT
    "全国地方公共団体コード"                        AS lg_code,
    "ID"                                              AS shelter_id,
    "名称"                                            AS name,
    NULLIF("名称_カナ", '')                          AS name_kana,
    NULLIF("名称_英字", '')                          AS name_en,
    "所在地_連結表記"                                AS address,
    "所在地_都道府県"                                AS pref,
    "所在地_市区町村"                                AS city,
    NULLIF("所在地_町字", '')                        AS town,
    NULLIF("所在地_番地以下", '')                    AS addr_detail,
    NULLIF("建物名等(方書)", '')                     AS building,
    "緯度"                                            AS latitude,
    "経度"                                            AS longitude,
    "標高"                                            AS elevation,
    NULLIF("電話番号", '')                           AS phone,
    NULLIF("内線番号", '')                           AS phone_ext,
    NULLIF("連絡先メールアドレス", '')               AS email,
    NULLIF("連絡先FormURL", '')                      AS form_url,
    NULLIF("連絡先備考（その他、SNSなど）", '')      AS contact_note,
    NULLIF("郵便番号", '')                           AS postal_code,
    "地方公共団体名"                                  AS lg_name,
    "災害種別_洪水" = '1'                             AS flood,
    "災害種別_崖崩れ、土石流及び地滑り" = '1'        AS landslide,
    "災害種別_高潮" = '1'                             AS storm_surge,
    "災害種別_地震" = '1'                             AS earthquake,
    "災害種別_津波" = '1'                             AS tsunami,
    "災害種別_大規模な火事" = '1'                     AS large_fire,
    "災害種別_内水氾濫" = '1'                         AS inland_flooding,
    "災害種別_火山現象" = '1'                         AS volcanic,
    NULLIF("指定避難所との重複", '')                 AS duplicated_with_shelter,
    "想定収容人数"                                    AS capacity,
    NULLIF("対象となる町会・自治会", '')             AS target_areas,
    NULLIF("URL", '')                                 AS url,
    NULLIF("画像", '')                                AS image_url,
    NULLIF("画像_ライセンス", '')                    AS image_license,
    NULLIF("備考", '')                                AS note
FROM {{ ref('raw_tsukuba_emergency_shelter_20241201') }}
